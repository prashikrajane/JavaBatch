public class Datatypes {

    public static void main(String[] args){

        int a = 1000000000;                     // 4 byte
        long l = 100000002121l;                 // 8 byte
        float f = 10054.4445698f;               // 4 byte
        double d = 10064.4655465464;            // 8 byte
        short s = 5421;                         // 2 byte
        byte b = 127;                           // 1 byte
        char c = 'a';                           // 2 byte
        boolean b1 = false;                     // 2 bit
        String str = "hello";

        System.out.println(d);
    }
}
